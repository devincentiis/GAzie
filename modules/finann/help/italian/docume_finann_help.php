<h3>Men&ugrave; Fine anno</h3>
<p class="help-text">Qui si accede alle funzioni
relative alle operazioni che si svolgono comunemente alla fine dell'anno
di gestione. In particolare, da qui &egrave; possibile ottenere un
bilancio e produrre la chiusura e riapertura dei conti in
contabilit&agrave; generale.</p>
</div>

