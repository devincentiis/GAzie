	</div>
	
	<div id="Clienti" class="tab-pane fade in">
	    <p>Da qui &egrave; possibile accedere alle anagrafiche dei clienti ed
		eventualmente gestire delle ricevute di pagamento, quando si
		riscuotono i crediti. Per inserire un nuovo cliente si deve
		scegliere la voce <strong>Nuovo cliente</strong>; per conoscere lo
		stato dei crediti verso clienti si deve selezionare la voce
		<strong>Lista dei crediti</strong>; per accedere all'elenco dei
		clienti, occorre selezionare invece la voce principale.</p>

		<p>Dall'elenco dei clienti, oppure dalla lista dei crediti,
		è possibile accedere a una maschera di inserimento delle
		riscossioni: in questo modo si ottiene la contabilizzazione
		della riscossione ed eventualmente anche la stampa di una
		ricevuta. L'elenco delle ricevute emesse si ottiene
		dalla voce <strong>Ricevute di pagamento</strong>.</p>
	</div>
	
	<div id="Contratti" class="tab-pane fade in">
		<p>Da qui &egrave; possibile gestire dei contratti per la fornitura
		di servizi, a cui fanno seguito delle fatture o delle ricevute di
		pagamento periodiche. Attraverso la voce <strong>Nuovo
		contratto</strong> si annota il testo di un contratto, assieme ai
		suoi estremi, mentre dalla voce principale si accede all'elenco dei
		contratti già inseriti, con la facoltà di modificarli e di stamparli.
		Con la voce <strong>Genera ricevute/fatture da contratti</strong>
		si ottiene la generazione delle ricevute o delle fatture relative
		alle scadenze previste dai contratti.</p>
	</div>
	
	<div id="Agenti" class="tab-pane fade in">
		<p>Da qui &egrave; possibile accedere all'elenco degli agenti di
		vendita: attraverso la voce principale si ottiene effettivamente
		l'elenco degli agenti gi&agrave; inseriti, mentre dalla voce
		<strong>Nuovo agente</strong> se ne possono inserire di nuovi. Va
		osservato che gli agenti devono già essere catalogati in
		qualit&agrave; di fornitori, prima di poter essere immessi
		nell'elenco degli agenti.</p>

		<p>Quando si inserisce o si modifica un agente, si specifica la
		provvigione predefinita, ma poi &egrave; possibile indicare una
		provvigione differente per un certo gruppo di articoli (specificando
		le categoria merceologiche o gli articoli). In ogni caso,
		l'associazione degli agenti avviene nell'anagrafica dei clienti (a
		ogni cliente può essere abbinato il proprio agente), tuttavia, in
		fase di inserimento della fattura questa associazione può essere
		modificata.</p>

		<p>Per ottenere la stampa delle provvigioni, occorre richiederlo
		dall'elenco degli agenti, selezionando l'icona della stampante
		che appare nella colonna <strong>Lista provvigioni</strong>.</p>
	</div>
