    <div id="Preventivi" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile emettere dei preventivi a clienti,
		attraverso una maschera di inserimento equivalente a quella di una
		fattura immediata normale. Il preventivo ha poi un aspetto finale
		molto simile a quello di una fattura.</p>

		<p class="help-text">Per inserire un nuovo preventivo si deve scegliere la voce
		<strong>Preventivo a cliente</strong>, mentre per accedere ai
		preventivi già emessi in precedenza, si deve selezionare la voce
		principale di questo men&ugrave;. Va osservato che il fatto di avere
		emesso un preventivo non comporta automatismi nei confronti del
		resto della gestione delle vendite.</p>
	</div>
	
	<div id="Ddt" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile gestire i documenti di trasporto (DDT).
		Per inserire un nuovo documento si deve scegliere la voce
		<strong>Emetti D.d.t.</strong>, mentre per accedere ai documenti già
		inseriti in precedenza, si deve selezionare la voce principale di
		questo men&ugrave;.</p>
	
		<p class="help-text">La maschera di emissione di un DDT &egrave; praticamente uguale
		a quella di una fattura immediata accompagnatoria.</p>

		<p class="help-text">Esempio: <br>
		- <a target="_blank" title="guarda il video"
		href="http://www.youtube.com/watch?v=odXYSakibw0">emissione di un
		documento di trasporto di vendita</a><br></p>
	</div>

	<div id="Ordini" class="tab-pane fade in">	
	    <p class="help-text">Da qui &egrave; possibile recepire gli ordini effettuati dai
		clienti, emettendo contestualmente delle conferme d'ordine,
		attraverso una maschera di inserimento equivalente a quella di una
		fattura immediata accompagnatoria. La conferma d'ordine ha poi un
		aspetto finale molto simile a quello di una fattura.</p>

		<p class="help-text">Per inserire una conferma d'ordine si deve scegliere la voce
		<strong>Ordine da cliente</strong>, mentre per accedere agli
		ordini già annotati in precedenza, si deve selezionare la voce
		principale di questo men&ugrave;. L'ordine recepito può essere
		usato per generare automaticamente un DDT oppure una fattura
		immediata, attraverso la voce <strong>Evadi ordini</strong>.</p>

		<p class="help-text">Esempi: <br>
		- <a target="_blank" title="guarda il video"
		href="http://www.youtube.com/watch?v=YpAIVbrS1EE">conferma d'ordine</a><br>
		- <a target="_blank" title="guarda il video"
		href="http://www.youtube.com/watch?v=xR39uw1_-Yk">evasione di un ordine</a></p>
	</div>
	
	<div id="Effetti" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile gestire gli effetti attivi da emettere
		a seguito di fatture di vendita che li prevedono in base al tipo di
		pagamento. Pertanto, generalmente gli effetti attivi vengono
		generati dalla procedura, per le fatture ancora da regolare.</p>
	
		<p class="help-text">Per emettere manualmente un nuovo effetto attivo si pu&ograve;
		scegliere la voce <strong>Emetti effetto</strong>; per generare
		automaticamente gli effetti derivanti dai tipi di pagamento
		previsti nelle fatture di vendita, si deve scegliere la voce
		<strong>Genera effetti da fatture</strong>; per produrre la distinta
		degli effetti cartacei da presentare in banca per l'incasso, si deve
		scegliere la voce <strong>Distinta effetti</strong>, mentre
		per produrre il file delle RiBa si deve scegliere la voce
		<strong>Genera file RiBa</strong>; per aggiornare
		la contabilit&agrave; generale si deve scegliere la voce
		<strong>Contabilizza effetti</strong>. Per visualizzare
		invece l'elenco degli effetti disponibili, occorre
		selezionare la voce principale.</p>
	
		<p class="help-text">Va osservato che la contabilizzazione degli effetti pu&ograve;
		avvenire solo per quelli che sono stati indicati in una distinta
		effetti o in file RiBa.</p>
	
		<p class="help-text">Esempi:<br>
		- <a target="_blank" title="guarda il video"
		href="http://www.youtube.com/watch?v=S0Iqgfh9mZs">generazione degli
		effetti da fatture</a><br>
		- <a target="_blank" title="guarda il video"
