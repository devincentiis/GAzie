	</div>

	<div id="Ddt" class="tab-pane fade in">
	    <p class="help-text">Da qui &egrave; possibile gestire i documenti di trasporto emessi
		per beni inviati a fornitore (per reso o in conto lavorazione), ma
		eventualmente è possibile annotare anche i DDT ricevuti da
		fornitore, se se ne ravvisa l'opportunità.</p>
	
		<p class="help-text">&Egrave; importante tenere in considerazione che la registrazione
		di un DDT di acquisto, comporta generalmente l'aggiornamento
		automatico della contabilit&agrave; di magazzino, ma oltre a
		ci&ograve;, pu&ograve; portare alla produzione di una registrazione
		in contabilit&agrave; generale, come se si trattasse di una fattura
		di acquisto. A tale proposito, &egrave; necessario ponderare bene
		l'utilit&agrave; di utilizzare la funzione <strong>Ricevuto Ddt di
		acquisto</strong>, piuttosto di limitarsi al carico manuale della
		merce in contabilit&agrave; di magazzino, come si dovrebbe in
		generale.</p>

		<p class="help-text">Esempio: <br/>
		- <a target="_blank" title="guarda il video"
		href="http://www.youtube.com/watch?v=8NrlW_EaTrs">emissione di un
		DDT di reso a fornitore</a></p>
	</div>

    <div id="Fatture" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile assimilare le fatture e le note di
		accredito emesse dai fornitori. Tuttavia si tratta di una
		facolt&agrave;, in quanto normalmente questi documenti andrebbero
		registrati in contabilit&agrave; generale ed eventualmente in
		contabilit&agrave; di magazzino, direttamente.</p>

		<p class="help-text">L'acquisizione qui di una fattura o di una nota di accredito ha
		il vantaggio di permettere la generazione automatica della scrittura
		in contabilit&agrave; generale, ma ha lo svantaggio di aggiornare
		anche il magazzino in modo automatico, quando questo potrebbe
		risultare gi&agrave; aggiornato dall'acquisizione di un DDT di
		acquisto o dall'emissione di un DDT di reso. A tale proposito,
		&egrave; necessario ponderare bene l'utilit&agrave; di utilizzare le
		funzioni <strong>Registra fattura di acquisto</strong> e
		<strong>Registra nota credito da fornitore</strong>, piuttosto di
		limitarsi all'aggiornamento manuale della contabilit&agrave;
		generale ed eventualmente anche della contabilit&agrave; di
		magazzino, come si dovrebbe in generale.</p>
	</div>

    <div id="BonAdd" class="tab-pane fade in">
	    <p>Da qui si ottiene l'elenco dei bonifici addebitati, relativi ai
		pagamenti ai fornitori. Questo elenco viene prodotto dalle funzioni
		di pagamento relative all'elenco dei fornitori o a quello dei
		debiti, descritti successivamente.</p>
	</div>


    <div id="Fornitori" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile accedere alle anagrafiche dei fornitori
		ed eventualmente controllare i pagamenti loro dovuti. Per inserire
		un nuovo fornitore si deve scegliere la voce <strong>Nuovo
		fornitore</strong>, mentre per accedere all'elenco dei fornitori
		occorre selezionare invece la voce principale.</p>

		<p>Dall'elenco dei fornitori è possibile accedere a una maschera di
		inserimento dei pagamenti da effettuare, con la quale si pu&ograve;
		ottenere la stampa di un ordine di bonifico alla propria banca,
		ottenendo contestualmente la registrazione del pagamento in
		contabilit&agrave; generale. Gli ordini di bonifico ottenuti in
		questo modo, sono visibili dalla funzione
		<strong>Bonifici-addebiti</strong>.</p>
	</div>
	
	<div id="Debiti" class="tab-pane fade in">
		<p>Da qui &egrave; possibile visualizzare l'elenco dei debiti verso
		fornitori, da come risulta dalla contabilit&agrave; generale, inoltre
		&egrave; possibile controllare i pagamenti, nello stesso modo descritto
		a proposito dell'elenco dei fornitori.</p>
	</div>
