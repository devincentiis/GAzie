		di magazzino. Per inserire un nuovo articolo si deve scegliere la voce
		<strong>Nuova merce/servizio</strong>, mentre per accedere agli
		articoli gi&agrave; esistenti, si deve selezionare la voce principale di
		questo men&ugrave;.</p>
	</div>

	<div id="CatMerc" class="tab-pane fade in">
		<p class="help-text">Gli articoli di magazzino sono raggruppati in categorie, a cui
		si accede da questa voce di men&ugrave;. Se non si intende suddividere
		gli articoli in gruppi, &egrave; comunque necessario definire almeno
		una categoria generica.</p>

		<p class="help-text">Per inserire una nuova categoria si deve scegliere la voce
		<strong>Nuova categoria merceologica</strong>, mentre per accedere
		alle categorie gi&agrave; esistenti, si deve selezionare la voce
		principale di questo men&ugrave;.</p>
	</div>

	<div id="Movimenti" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile intervenire nei movimenti del Quaderno di campagna
		(scritture di carico e scarico). Per inserire un nuovo movimento si
		deve scegliere la voce <strong>Nuova registrazione Quaderno di campagna</strong>, mentre per accedere ai movimenti gi&agrave;
		esistenti, si deve selezionare la voce principale di questo
		men&ugrave;.
		</p>
		<p class="help-text">Dai movimenti inseriti &egrave; possibile ottenere la stampa del
		Quaderno di campagna attraverso la voce <strong>Stampa Quaderno di campagna</strong>, mentre per ottenere una stampa dettagliata,
		articolo per articolo, si deve scegliere la voce <strong>Stampa
		schedari articoli di magazzino</strong>.
		</p>
		<p>La voce <strong>Costo produzione</strong> permetterà di conteggiare e visualizzare il costo totale di una specifica produzione agricola.
		</p>
	</div>

	<div id="Causali" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile gestire le causali usate per descrivere
		i movimenti di magazzino. Le causali predefinite, riconoscibili
		perch&eacute; sono scritte con un testo tutto maiuscolo, non vanno
		modificate, essendo utilizzate automaticamente dalla procedura;
		tuttavia, si possono creare altre causali, in base alla propria
		convenienza.
		</p>
		<p class="help-text">Per inserire una nuova causale si deve scegliere la voce
		<menuitem>Nuova causale di magazzino</menuitem>, mentre per accedere
		alle causali gi&agrave; esistenti, si deve selezionare la voce
		principale di questo men&ugrave;.
		</p>
	</div>

	<div id="Campi" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile inserire i campi coltivati specificandone le dimensioni e il tipo di coltivazione in atto. Inoltre &egrave; possibile stampare la dichiarazione di rame metallo utilizzato in ogni singolo campo di coltivazione.
		</p>
		<br>
	</div>
	<div id="Fitofarmaci" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile gestire le tabelle <strong>Avversità</strong> e <strong>Colture</strong>. La compilazione di queste tabelle servirà per la successiva compilazione della tabella <strong>Uso fitofarmaci</strong>. Tale tabella permette di effettuare i controlli delle dosi specifiche per fitofarmaci, colture e avversità.</p><p> Sempre da questo menu sarà possibile aggiornare la tabella <strong>Fitofarmaci</strong> connettendosi al database del Ministero della salute. Questo aggiornamento necessita di connessione ad internet. Tale tabella verrà utilizzata per l'inserimento di un nuovo fitofarmaco nelle merci/servizi e per i dovuti controlli sulle scadenze delle autorizzazioni dei fitofarmaci utilizzati in azienda.
		</p>
		<br>
	</div>
	<div id="SIAN" class="tab-pane fade in">
		<p class="help-text">Da qui &egrave; possibile gestire, tramite file di upload, il Registro di carico e scarico del Portale dell'olio di oliva del SIAN (Sistema Informatico Agricolo Nazionale).
		</p>
		<p>Con i file di upload sarà  possibile inviare i movimenti di carico e scarico, le anagrafiche dei clienti e fornitori, gli stabilimenti/depositi e i recipienti di stoccaggio.
		</p>
		<p>Infine, in tempo reale sarà visibile la situazione di tutti i recipienti attivi con i relativi contenuti.
		</p>
